package tw.bcroom335.javaproject.operators;

public class TestOperatorshiftEX1 {

	public static void main(String[] args) {
		int Num1=1234;
		int Num2=-1234;
		
		System.out.println("Num1"+">>2="+(Num1>>2));
		System.out.println("Num2"+">>2="+(Num2>>2));
		
		System.out.println("Num1"+"<<2="+(Num1<<2));
		System.out.println("Num2"+"<<2="+(Num2<<2));
		
		System.out.println("Num1"+">>>2="+(Num1>>>2));
		System.out.println("Num2"+">>>2="+(Num2>>>2));
		

	}

}
