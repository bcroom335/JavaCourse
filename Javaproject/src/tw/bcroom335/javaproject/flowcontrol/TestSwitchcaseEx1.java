package tw.bcroom335.javaproject.flowcontrol;

public class TestSwitchcaseEx1 {

	public static void main(String[] args) {
		int member=3;
		switch(member) {		            
		            case 1 :
		            	System.out.println("ordinary member.");
		            	break;
		            case 2 :
		            	System.out.println("silver member.");
		            	break;		            
		            case 3 :
		            	System.out.println("gold member.");
		            	break;
		            default :
        	            System.out.println("guest.");
        	            break;
		            		            	       
		}
		System.out.println("Keep Writing JAVA~~~~.");

	}

}
